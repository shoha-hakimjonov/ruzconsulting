function setLanguage(language) {

  var x = getCookie('lang');
  if (x) {
    if (x != language) {
      eraseCookie('lang');
      setCookie('lang', language, 7);
    }
  } else {
    setCookie('lang', language, 7);
  }
  document.documentElement.lang = language;
  var currentLanguage = getCookie('lang');

  app.lang = app.translations[currentLanguage] || app.translations.en;

  changeLanguageSwitchFunction(currentLanguage);
//   changeLogo(currentLanguage);
  for (const property in app.lang) {

    if (document.getElementById(property) != null) {

      document.getElementById(property).innerHTML = app.lang[property];
    //   document.getElementById(property).style.fontFamily = 'OpenSans';
    }
  }
}

function changeLanguageSwitchFunction(language) {
    if(language == "en") {
        document.getElementById("languageSwitch").setAttribute('onclick', "setLanguage('ru')");
    } 
    else if (language == "ru") {
        document.getElementById("languageSwitch").setAttribute('onclick', "setLanguage('en')");
    }
}

function setCookie(name, value, days) {
  var expires = "";
  if (days) {
    var date = new Date();
    date.setTime(date.getTime() + (days * 24 * 60 * 60 * 1000));
    expires = "; expires=" + date.toUTCString();
  }
  document.cookie = name + "=" + (value || "") + expires + "; path=/";
}
function getCookie(name) {
  var nameEQ = name + "=";
  var ca = document.cookie.split(';');
  for (var i = 0; i < ca.length; i++) {
    var c = ca[i];
    while (c.charAt(0) == ' ') c = c.substring(1, c.length);
    if (c.indexOf(nameEQ) == 0) return c.substring(nameEQ.length, c.length);
  }
  return null;
}
function eraseCookie(name) {
  document.cookie = name + '=; Path=/; Expires=Thu, 01 Jan 1970 00:00:01 GMT;';
}